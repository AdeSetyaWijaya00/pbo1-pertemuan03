package Percabangan;

public class IfElse {
    public static void main(String[] args) {
        int x = 5;

        if (x > 6) System.out.println("x>i");
        else if (x <= 4)
            System.out.println("x<=4");
        else if (x < 10)
            System.out.println("x<10");
        else
            System.out.println("bukan dari salah satu di atas");
    }
}

package Percabangan;

public class SwitchChar {
    public static void main(String[] args) {

        char lampu ='M';
        switch (lampu){
            case 'M':
                System.out.println("Lampu Merah, Berhenti !!!");
                break;
            case 'K':
                System.out.println("Lampu Kuning, Hati-hati !!!");
                break;
            case 'H':
                System.out.println("Lampu Hijau, Silahkan Jalan !!!");

        }
    }
}
